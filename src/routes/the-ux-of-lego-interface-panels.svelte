<blockquote>
<p>Most interfaces in our world contain a blend of digital screens and analog inputs like switches and dials</p>
</blockquote>
<blockquote>
<p>How can we differentiate between the function of different inputs?
How can we organise the many inputs and outputs so that we understand how they relate to each other?</p>
</blockquote>
<p>Designing interfaces is about wrapping with the above two questions, understanding relationships and functions. This applies for GUIs or CLIs, end user or developer experiences. Those are the two big human/machine questions. </p>
<p>Tools for differentiating include shape coding:</p>
<blockquote>
<p>The right panel (“Aircraft Multiple Flight Controls”) clearly distinguishes between the throttle (large, linear vertical movement), toggle switches (round vertical flick) and the push buttons (square push-in).</p>
</blockquote>
<p>Along with:</p>
<blockquote>
<p>Colour coding is perhaps the only one to break into our everyday vocabulary, but we can add four more: size, texture, position and operation coding. Together these six are our allies in the design of error-proof interfaces.</p>
</blockquote>
<blockquote>
<p>Differentiation is a good first step that will avoid confusion between adjacent switches. But its only with organisation that we can create a clear and accurate mental model of the interface for the user.</p>
</blockquote>
<p>Differentiation support organization. </p>
<p>Organizational techniques include distributed and consolidated:</p>
<blockquote>
<p>These panels are what I’d called a consolidated interface. Every piece of input and feedback has been moved onto the same panel. This is the approach that Dyson took with their car. Now imagine the opposite, moving each of those lights and switches to the actual location of that valve in the factory. Sounds ludicrous, but these air vents in the Audi TT show that this distributed approach can also be a great win for user experience. I wrote a lot more about these distributed interfaces last year.</p>
</blockquote>
<p>Feature based, operation based, technology based. </p>
<blockquote>
<p>Any touchscreen with buttons by the side exhibits this technology-based split. In a future world, SpaceX might embed these physical controls right inside the screen next to the information they affect, but for now they sit awkwardly by the side as if nothing is wrong.</p>
</blockquote>
<p>Technology based organization is not human centered, and therefor not ideal UX. </p>
