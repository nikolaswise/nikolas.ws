<p>I decided it was about time to just write this down somewhere I can always find it. Below is the recipe for old Shanghai style eggnog, or as I refer to it: <em>High Octane Nogahol</em>. I tend to omit the optional ingredients at the end, and just serve the ‘nog without the additional fluff.</p>
<p>This recipe is 8:5 dairy:alchohol, with 8 parts bourbon to 1 part brandy or cognac, and 1 part rum.</p>
<h2>Ingredients</h2>
<p>12 large eggs
2 cups sugar
1 cup heavy cream
1 quart whole milk
1 liter bourbon
1/2 cup dark rum
1/2 cup cognac or brandy</p>
<h2>Instructions</h2>
<ol>
<li>
<p>Whisk 12 egg yolks with sugar.</p>
</li>
<li>
<p>Add the cream, milk, bourbon, rum, and cognac.</p>
</li>
<li>
<p>Age. Or don’t!</p>
</li>
<li>
<p>Serve with a little nutmeg grated on top.</p>
</li>
</ol>
