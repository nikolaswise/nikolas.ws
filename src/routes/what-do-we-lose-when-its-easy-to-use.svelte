<Introduction {metadata} />
<p>Pressman runs a local design studio called Rumors. In 2015 they launched a new site that got a lot of critique on Designer News about its ‘usability’. Pressman created an experience when preview thumbnails exist behind the text and flee from the cursor.</p>
<blockquote>
<p>I hate the word usability. I hate that it’s squishy, poorly defined … all I can come to is “ease of use”. Is the interface easy to use or easy to understand?</p>
</blockquote>
<p>Pressman wonders who the implied “for whom” is in the statement.</p>
<blockquote>
<p>All these sites where all being built on the same framework and had the same look and feel.</p>
</blockquote>
<p>Pressman explores ‘period styles.’ He examines that web design has clear period styles which are tightly coupled to technological developments. Early web was focused on tables, due to the current tech. Then Bootstrap.css became a thing, and it moved quickly into its own period style. Bootstrap as a framework caused a development of a signature product style.</p>
<blockquote>
<p>The more familiar the interface the less cognitive load … users grow accustomed to a set of behaviors … and take the desired action.</p>
</blockquote>
<blockquote>
<p>Did the user do the thing?</p>
</blockquote>
<p>Pressman explores how the focus on conversions became the defining impulse to design decisions.</p>
<blockquote>
<p>Homogeneity reduces friction.</p>
</blockquote>
<p>Friction, and the desire to remove friction for the sake of conversions, drove a uniformity of a period style.</p>
<p>Pressman offers an example from Speikermann who thinks the web should be alike a book – which is a classic Speikermann take.</p>
<p>Pressman instead says that the web is a “medium that we inhabit” – and avoiding any friction “reduces the medium to a series of consumer efficiencies.” This reduction has strong similarities to the reduction of our current age – the deletion and removal of anything that doesn’t immediately and efficiently serve the needs of capital.</p>
<blockquote>
<p>The web is full of terrible, frustrating experiences … ease of use can and should be empowering.</p>
</blockquote>
<p>There are some places – and functions of society – where ease of use is a powerful tool that <em>should</em> be prioritized.</p>
<p>However, “frictions and inefficiencies are the moments that slow us down”. This means that <em>intentionally</em> creating friction, erecting a barrier to slow down the user before hitting the endpoint “allows the user to process an experience rather than just consume it.” Pressman quotes <a
  href="https://laurelschwulst.com/"
  rel="nofollow"
>Laurel Schwulst</a> questions:</p>
<blockquote>
<p>Is it specific?
Is it memorable?
Does it turn idea into form?</p>
</blockquote>
<p>Usability cannot be the <em>sole</em> criteria of of “good”, and these are a good starting place to expand our practice. Pressman adds a fourth:</p>
<blockquote>
<p>Does it benefit the user?</p>
</blockquote>
<p>Pressman notes that none of these preclude ease of use, but they do focus on experience rather than outcome. Pressman suggests that <a
  href="https://www.mcmaster.com/"
  rel="nofollow"
>https://www.mcmaster.com/</a> is the very best website on the internet. I agree! This is an amazing site.</p>
<blockquote>
<p>This response to the literacy of its users is what allows the site to be usable.</p>
</blockquote>
<p>Pressman talk about the use style, which at its core is a vernacular language that allows you to efficiently speak to others that understand the same vernacular. This culls, rather than grows, an audience. Style performs the same function as jargon here, and acts as a way to attune a signal to a receiver. A reduction of the broadcast to the narrow-band communication.</p>
<blockquote>
<p>Brands depend on mental models of value.</p>
</blockquote>
<p>Pressman talks about how the process of learning to use a difficult thing creates a sense of ownership. Pressman sees the value in creating and rewarding experience.</p>
<p>Pressman gives the example of Printed Matter, and calls out that the deliberate introduction of friction can communicate a significant amount of <em>value</em> and <em>intention</em>. By creating friction in a certain way, by deliberately moving away from the frictionless standard, it invites the question of “why did we do this?“. If that question can be answered from a place of meaning, your values are communicated to the audience, and you can filter down a generic audience to a specific one that shares those values.</p>
<blockquote>
<p>Meaning and form aren’t just seen. Form is the behavior.</p>
</blockquote>
<p>This relates back to the fundamental stuff-ness of the web.</p>
<p>The fourth point about benefit is about ethics – and creating a baseline ethical behavior for the interactions. This is the basic opening of web developments ethical compass: does it do what the user wants to do?</p>
<blockquote>
<p>A dark pattern is frictionless design taken to its logical conclusion.</p>
</blockquote>
<p>An example of how the shifting rubrics and definitions of “good” are fundamental to the execution and creation of any designed work. MetaFilter is a powerful example of creating a lot of friction <em>against</em> rapid growth. This creates a benefit for the broader community. Against twitter, it’s clear where values lie and the ethical ramifications of sets of decisions.</p>
<blockquote>
<p>The Frustration Threshold: How much friction can we add before it becomes frustration?</p>
</blockquote>
<p>This is directly correlated to the specificity of the users goals. When looking for a phone number or address, the threshold is very very low. It’s also correlated to the scale of the audience, which maps back to the use of values as a filtering mechanism.</p>
<blockquote>
<p>The more you understand of [the users needs] the more difficult and complex an experience you can build</p>
</blockquote>
<p>Pressman ends the talk by framing the central question that he wants us to consider: Instead of “How can I make everything easy to use?” ask “Just what components can be difficult?“.</p>
